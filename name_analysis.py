def analyze_name(name):
    """分析名字的五行分布"""
    if not name:
        return "请输入姓名"
    
    # 统计五行分布
    wuxing_count = {'木': 0, '火': 0, '土': 0, '金': 0, '水': 0}
    
    for char in name:
        for element, chars in WUXING_MAP.items():
            if char in chars:
                wuxing_count[element] += 1
    
    # 计算五行强度
    total_chars = len(name)
    wuxing_strength = {}
    for element, count in wuxing_count.items():
        if total_chars > 0:
            wuxing_strength[element] = round((count / total_chars) * 100, 1)
        else:
            wuxing_strength[element] = 0
    
    # 找出主要五行
    main_element = max(wuxing_count, key=wuxing_count.get)
    
    # 生成分析结果
    result = f"姓名: {name}\n\n五行分布:\n"
    for element, percentage in wuxing_strength.items():
        result += f"{element}: {percentage}%\n"
    
    result += f"\n主要五行: {main_element}\n"
    
    # 相生相克分析
    result += f"\n相生关系: {main_element} → {WUXING_XIANGSHENG[main_element]}\n"
    result += f"相克关系: {main_element} → {WUXING_XIANGKE[main_element]}\n"
    
    # 五行特点
    wuxing_features = {
        '木': '木主仁，性情温和，有爱心，喜欢自然，重视人际关系。',
        '火': '火主礼，热情开朗，有活力，喜欢社交，富有创造力。',
        '土': '土主信，稳重踏实，诚实守信，重视家庭，有责任感。',
        '金': '金主义，坚强果断，有正义感，注重效率，追求成功。',
        '水': '水主智，聪明灵活，思维敏捷，适应能力强，富有智慧。'
    }
    
    result += f"\n{wuxing_features[main_element]}"
    
    return result

